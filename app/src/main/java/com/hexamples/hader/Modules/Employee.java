package com.hexamples.hader.Modules;

public class Employee {

    String status,UserName,Password,Staff_Name,Staff_Phone,Staff_Email,Staff_Level,Staff_Address,msg;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getUserName() {
        return UserName;
    }

    public void setUserName(String userName) {
        UserName = userName;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String password) {
        Password = password;
    }

    public String getStaff_Name() {
        return Staff_Name;
    }

    public void setStaff_Name(String staff_Name) {
        Staff_Name = staff_Name;
    }

    public String getStaff_Phone() {
        return Staff_Phone;
    }

    public void setStaff_Phone(String staff_Phone) {
        Staff_Phone = staff_Phone;
    }

    public String getStaff_Email() {
        return Staff_Email;
    }

    public void setStaff_Email(String staff_Email) {
        Staff_Email = staff_Email;
    }

    public String getStaff_Level() {
        return Staff_Level;
    }

    public void setStaff_Level(String staff_Level) {
        Staff_Level = staff_Level;
    }

    public String getStaff_Address() {
        return Staff_Address;
    }

    public void setStaff_Address(String staff_Address) {
        Staff_Address = staff_Address;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        msg = msg;
    }
}
